    private void cargar(){
        DefaultTableModel modelo = new DefaultTableModel();
        try {
            jtProductos.setModel(modelo);
            PreparedStatement ps = null;
            ResultSet rs = null;

            String sql = "SELECT nomProv,nombreMed,cant from compra c,provedor p,medicamento m where p.idProvedor = c.Provedor_idProvedor and m.idMedicamento = c.Medicamento_idMedicamento";
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();

            ResultSetMetaData rsMd = (ResultSetMetaData) rs.getMetaData();
            int cantidadColumnas = rsMd.getColumnCount();

            modelo.addColumn("Proveedor");
            modelo.addColumn("Medicamento");
            modelo.addColumn("Cantidad");

            int[] anchos = {50, 50, 50};
            for (int i = 0; i < jtProductos.getColumnCount(); i++) {
                jtProductos.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
            }

            while (rs.next()) {
                Object[] filas = new Object[cantidadColumnas];
                for (int i = 0; i < cantidadColumnas; i++) {
                    filas[i] = rs.getObject(i + 1);
                }
                modelo.addRow(filas);
            }

        } catch (SQLException ex) {
            System.err.println(ex.toString());
        }
    }
